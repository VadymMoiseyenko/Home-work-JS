let circuit = (radius) => 2 * Math.PI * radius;
console.log(circuit(6));

let calculateArea = (width, height) => {
    return width * height;
};
console.log(calculateArea(5, 5));

let areaSquare = function (widht) {
    return Math.pow(widht, 2);
};
console.log(areaSquare(7));

function conversionValute(suma, kurs) {
    return suma * kurs;
}
console.log(conversionValute(300, 27))

